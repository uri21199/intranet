function agregarFechasAprobadas(eventsArr, fechasAprobadas) {
    // Itera sobre cada empleado en fechasAprobadas
    for (let empleado in fechasAprobadas) {
      if (fechasAprobadas.hasOwnProperty(empleado)) {
        // Itera sobre los tipos de día para cada empleado
        for (let tipoFecha in fechasAprobadas[empleado]) {
          if (fechasAprobadas[empleado].hasOwnProperty(tipoFecha)) {
            let fechas = fechasAprobadas[empleado][tipoFecha];
  
            // Verifica si fechas es un array antes de iterar sobre él
            if (Array.isArray(fechas)) {
              // Si el tipo de fecha es 'Ausencia' o 'Vacaciones'
              if (tipoFecha === 'Ausencia' || tipoFecha === 'Vacaciones') {
                // Itera sobre cada rango de fechas
                fechas.forEach(function(fechaRange) {
                  let startDate = new Date(fechaRange[0]);
                  let endDate = fechaRange[1] ? new Date(fechaRange[1]) : startDate; // Usa fechaRange[1] si está definido, de lo contrario, usa startDate
  
                  // Ajusta la fecha de fin al día siguiente si es necesario
                  endDate.setDate(endDate.getDate() + 1);
                  console.log("esto es la fecha de fin" + endDate)
  
                  // Itera sobre las fechas entre la fecha de inicio y la fecha final
                  let currentDate = new Date(startDate);
                  while (currentDate <= endDate) {
                    // Si la fecha actual no es el día de inicio
                    if (currentDate.getDate() !== startDate.getDate()) {
                      // Crea un evento para la fecha actual
                      let eventYear = currentDate.getFullYear();
                      let eventMonth = currentDate.getMonth() + 1;
                      let eventDay = currentDate.getDate();
  
                      // Verifica si el evento ya existe en eventsArr
                      let existingEventIndex = eventsArr.findIndex(function(event) {
                        // Comprueba la fecha, el tipo de evento y el empleado
                        return event.day === eventDay &&
                          event.month === eventMonth &&
                          event.year === eventYear &&
                          event.events.some(function(ev) {
                            return ev.title === tipoFecha && ev.name === empleado;
                          });
                      });
  
                      // Si el evento no existe, agrégalo a eventsArr
                      if (existingEventIndex === -1) {
                        eventsArr.push({
                          day: eventDay,
                          month: eventMonth,
                          year: eventYear,
                          events: [{
                            title: tipoFecha,
                            name: empleado
                          }]
                        });
                      }
                    }
  
                    // Avanza al siguiente día
                    currentDate.setDate(currentDate.getDate() + 1);
                  }
                });
              } else {
                // Si el tipo de fecha no es 'Ausencia' o 'Vacaciones'
                // Itera sobre cada fecha en el arreglo
                fechas.forEach(function(fecha) {
                  // Divide la fecha en partes (año, mes, día)
                  let fechaParts = fecha.split('-');
                  let eventYear = parseInt(fechaParts[0]);
                  let eventMonth = parseInt(fechaParts[1]);
                  let eventDay = parseInt(fechaParts[2]);
  
                  // Verifica si el evento ya existe en eventsArr
                  let existingEventIndex = eventsArr.findIndex(function(event) {
                    // Comprueba la fecha, el tipo de evento y el empleado
                    return event.day === eventDay &&
                      event.month === eventMonth &&
                      event.year === eventYear &&
                      event.events.some(function(ev) {
                        return ev.title === tipoFecha && ev.name === empleado;
                      });
                  });
  
                  // Si el evento no existe, agrégalo a eventsArr
                  if (existingEventIndex === -1) {
                    eventsArr.push({
                      day: eventDay,
                      month: eventMonth,
                      year: eventYear,
                      events: [{
                        title: tipoFecha,
                        name: empleado
                      }]
                    });
                  }
                });
              }
            }
          }
        }
      }
    }
  }
  

