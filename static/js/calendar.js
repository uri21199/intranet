//function update events when a day is active
function updateEvents(date) {
  let events = "";

  eventsArr.forEach((event) => {
    if (
      date === event.day &&
      month + 1 === event.month &&
      year === event.year
    ) {
      event.events.forEach((ev) => {
        events += `<div class="event">
            <div class="title">
              <i class="fas fa-circle"></i>
              <h3 class="event-title">${ev.name}</h3>
            </div>
            <div class="event-time">
              <span class="event-time">${ev.title}</span>
            </div>
        </div>`;
      });
    }
  });

  if (events === "") {
    events = `<div class="no-event">
            <h3>No hay evento que mostrar.</h3>
        </div>`;
  }

  eventsContainer.innerHTML =
    events !== ""
      ? events
      : `<div class="no-event"><h3>No hay evento que mostrar.</h3></div>`;

  saveEvents();
}


